import 'package:flutter/material.dart';

Color clr(int colorNum) {
  switch (colorNum) {
    case 0:
      return Colors.white;
    case 1:
      return Color(0xff3fb6c8);
    case 2:
      return Color(0xff2F95A5);
    case 3:
      return Color(0xffD9D9D9);
    case 4:
      return Color(0xffe6b5fb);
    case 5:
      return Color(0xffed87ab);
    case 6:
      return Color(0xffeed197);
  }
  return Colors.black;
}
