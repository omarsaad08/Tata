import 'package:flutter/material.dart';

Color clr(int colorNum) {
  switch (colorNum) {
    case 0:
      return Colors.white;
    case 1:
      return Color(0xff3fb6c8);
    case 2:
      return Color(0xffed87ab);
    case 3:
      return Color.fromARGB(255, 197, 228, 233);
    case 4:
      return Color.fromARGB(255, 19, 53, 60);
  }
  return Colors.black;
}
