import 'package:flutter/material.dart';

Color clr(int colorNum) {
  switch (colorNum) {
    case 0:
      return Colors.white;
    case 1:
      return Color(0xff3fb6c8);
    case 2:
      return Color(0xffed87ab);
    case 3:
      return Color(0xffDEF3F7);
    case 4:
      return Color(0xffFFEBF1);
    case 5:
      return Color(0xffF6F6F6);
    case 6:
      return Color(0xff272838);
  }
  return Colors.black;
}
